class InputForm:
    """Kelas untuk merepresentasikan form input data mahasiswa."""
    
    def __init__(self):
        pass

    def input_nama(self):
        """Menginput nama mahasiswa."""
        return input("Masukkan nama mahasiswa: ")

    def input_nilai(self):
        """Menginput nilai mahasiswa."""
        return input("Masukkan nilai mahasiswa: ")